#include "libft.h"

char	*ft_strmapi(char const *s, char (*f) (unsigned int, char))
{
	char	*str;
	int		i;
	int		j;

	i = 0;
	if (s == NULL || f == NULL)
		return (NULL);
	j = ft_strlen((char *)s);
	if (!(str = (char *)malloc(sizeof(char) * (j + 1))))
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
