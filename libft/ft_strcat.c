#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int a;
	int b;

	b = 0;
	a = 0;
	while (dest[a])
		a++;
	while (src[b])
	{
		dest[a] = src[b];
		a++;
		b++;
	}
	dest[a] = '\0';
	return (dest);
}
