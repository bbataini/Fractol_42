#include "libft.h"

char	*ft_strrev(char *str)
{
	int		a;
	int		b;
	char	c;

	b = 0;
	a = 0;
	while (str[b] != '\0')
	{
		b++;
	}
	b--;
	while (a < b)
	{
		c = str[a];
		str[a] = str[b];
		str[b] = c;
		a++;
		b--;
	}
	return (str);
}
